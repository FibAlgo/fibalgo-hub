-- Social Proof Users Table
-- Stores fake users for social proof notifications
-- Names and flags are matched to realistic country-specific names

CREATE TABLE IF NOT EXISTS public.social_proof_users (
  id SERIAL PRIMARY KEY,
  name TEXT NOT NULL,
  country_code TEXT NOT NULL, -- ISO 3166-1 alpha-2 country code
  flag_emoji TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Create index for random selection
CREATE INDEX IF NOT EXISTS idx_social_proof_users_id ON public.social_proof_users(id);

-- Function to get a deterministic user based on current minute
-- This ensures all visitors see the same user at the same time
CREATE OR REPLACE FUNCTION get_social_proof_user_for_minute(p_minute INTEGER)
RETURNS TABLE(name TEXT, country_code TEXT, flag_emoji TEXT) AS $$
BEGIN
  RETURN QUERY
  SELECT sp.name, sp.country_code, sp.flag_emoji
  FROM public.social_proof_users sp
  WHERE sp.id = (p_minute % (SELECT COUNT(*)::INTEGER FROM public.social_proof_users)) + 1;
END;
$$ LANGUAGE plpgsql STABLE;

